    /**
     * Get a validator for an incoming registration request.
     *
     * @param  array  $data
     * @return \Illuminate\Contracts\Validation\Validator
     */
    protected function validator(array $data)
    {
        return Validator::make($data, [
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users'],
            'phone' => ['required', 'string', 'max:25', 'unique:users'],
            'position' => ['required', 'string', 'max:25']
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validator($request->all())->validate();

        $password = Str::random(10);
        $details = (object) $request->all();


        $create_user = DB::transaction(function () use ($details, $password) {


            $user = User::create([
                'name' => $details->name,
                'email' => $details->email,
                'phone' => $details->phone,
                'position' => $details->position,
                'password' => Hash::make($password)
            ]);

            if ($user == false) {
                # code...
                return false;
            }

            event(new NewUserSuccessfullyAddedByAdmin($user));

            return true;
        });


        if ($create_user == false) {
            # code...

            return redirect()->back()->with(['error' => "Error Creating new User.!"]);
        }

        return redirect()->back()->with(['success' => 'New User Successfully Created.']);
    }

